    public function upload(Request $request)
    {
        $request->validate([
            'upload' => 'required',
        ]);

        DB::beginTransaction();
        try {
            // Excel::import(new AttendanceUpload, $request->file('upload')->store('files'));
            (new AttendanceUpload)->queue($request->file('upload')->store('files'))->chain([]);

            DB::commit();
        } catch (Throwable $th) {
            DB::rollBack();
            throw $th;
        }

        return redirect('/');
    }

